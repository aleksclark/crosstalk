// DrawChar draws a single character at (x, y) on the image.
func DrawChar(img *image.RGBA, x, y int, ch byte, col image.Uniform) {
	if ch > 127 {
		ch = '?'
	}
	glyph := font6x10[ch]
	for row := 0; row < fontH; row++ {
		bits := glyph[row]
		for bit := 0; bit < fontW; bit++ {
			if bits&(1<<uint(fontW-1-bit)) != 0 {
				px := x + bit
				py := y + row
				if px >= 0 && px < img.Bounds().Max.X && py >= 0 && py < img.Bounds().Max.Y {
					img.SetRGBA(px, py, col.C.(color.RGBA))
				}
			}
		}
	}
}

// DrawString draws a string at (x, y).
func DrawString(img *image.RGBA, x, y int, s string, col image.Uniform) {
	cx := x
	for i := 0; i < len(s); i++ {
		DrawChar(img, cx, y, s[i], col)
		cx += fontW
	}
}

// StringWidth returns the pixel width of a string.
func StringWidth(s string) int {
	return len(s) * fontW
}
